def addition(val1, val2):
    res = val1 + val2
    return res

def subtract(val1, val2):
    res = val1 - val2
    return res

def division(val1, val2):
    res = val1 / val2
    return res

def multiply(val1, val2):
    res = val1 * val2
    return res

print(addition(100,10))
print(subtract(100,10))
print(division(100,10))
print(multiply(100,10))